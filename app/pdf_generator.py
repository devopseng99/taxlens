# Summary cover page (ReportLab — TaxLens branded)
# ---------------------------------------------------------------------------
def generate_summary_page(result: TaxResult) -> BytesIO:
    buf = BytesIO()
    c = canvas.Canvas(buf, pagesize=letter)
    y = HEIGHT - 40
    y = draw_header(c, "TaxLens", "Tax Return Summary", result.tax_year, y)

    # Filer info
    y = draw_section(c, "Filing Information", y)
    filer = result.filer
    status_labels = {"single": "Single", "mfj": "Married Filing Jointly",
                     "hoh": "Head of Household", "mfs": "Married Filing Separately"}
    y = draw_info_row(c, "Name:", f"{filer.first_name} {filer.last_name}", y)
    y = draw_info_row(c, "SSN:", filer.ssn, y)
    if result.filing_status == "mfj" and result.spouse:
        y = draw_info_row(c, "Spouse:", f"{result.spouse.first_name} {result.spouse.last_name}", y)
    if filer.address_street:
        y = draw_info_row(c, "Address:", filer.address_street, y)
        y = draw_info_row(c, "City/St/Zip:", f"{filer.address_city}, {filer.address_state} {filer.address_zip}", y)
    y = draw_info_row(c, "Filing Status:", status_labels.get(result.filing_status, result.filing_status), y)
    if result.num_dependents > 0:
        if result.dependents:
            dep_names = ", ".join(f"{d.first_name} {d.last_name}" for d in result.dependents if d.first_name)
            y = draw_info_row(c, "Dependents:", f"{result.num_dependents} — {dep_names}" if dep_names else str(result.num_dependents), y)
        else:
            y = draw_info_row(c, "Dependents:", str(result.num_dependents), y)
    y -= 5

    # Federal summary
    y = draw_section(c, "Federal Tax Summary (Form 1040)", y)
    y = draw_line(c, "", "Total income", result.line_9_total_income, y)
    if result.sched_c_total_profit > 0:
        y = draw_line(c, "", "  Business income (Schedule C)", result.sched_c_total_profit, y)
    y = draw_line(c, "", "Adjusted gross income", result.line_11_agi, y)
    y = draw_line(c, "", f"Deduction ({result.deduction_type})", result.line_13_deduction, y)
    if result.qbi_deduction > 0:
        y = draw_line(c, "", "  QBI deduction (Section 199A)", result.qbi_deduction, y)
    y = draw_line(c, "", "Taxable income", result.line_15_taxable_income, y)
    if result.se_tax > 0:
        y = draw_line(c, "", "  Self-employment tax", result.se_tax, y)
    if result.niit > 0:
        y = draw_line(c, "", "  Net Investment Income Tax (3.8%)", result.niit, y)
    if result.additional_medicare_tax > 0:
        y = draw_line(c, "", "  Additional Medicare Tax (0.9%)", result.additional_medicare_tax, y)
    y = draw_line(c, "", "Total federal tax", result.line_24_total_tax, y, bold=True)
    # Credits
    has_credits = (result.line_27_ctc > 0 or result.education_credit > 0 or
                   result.education_credit_refundable > 0 or result.cdcc > 0 or
                   result.savers_credit > 0 or result.eitc > 0)
    if has_credits:
        y = draw_section(c, "Credits", y)
        if result.line_27_ctc > 0:
            y = draw_line(c, "", "  Child Tax Credit", result.line_27_ctc, y)
        if result.education_credit > 0:
            y = draw_line(c, "", "  Education credits (nonrefundable)", result.education_credit, y)
        if result.education_credit_refundable > 0:
            y = draw_line(c, "", "  Education credits (refundable AOTC)", result.education_credit_refundable, y)
        if result.cdcc > 0:
            y = draw_line(c, "", "  Child & Dependent Care Credit", result.cdcc, y)
        if result.savers_credit > 0:
            y = draw_line(c, "", "  Saver's Credit", result.savers_credit, y)
        if result.eitc > 0:
            y = draw_line(c, "", "  Earned Income Tax Credit", result.eitc, y)

    if result.estimated_tax_penalty > 0:
        y = draw_line(c, "", "  Estimated tax penalty", result.estimated_tax_penalty, y)
    if result.amt > 0:
        y = draw_line(c, "", "  Alternative Minimum Tax", result.amt, y)

    y = draw_line(c, "", "Total withholding + payments", result.line_33_total_payments, y)
    if result.line_34_overpaid > 0:
        y = draw_line(c, "", "FEDERAL REFUND", result.line_34_overpaid, y, bold=True)
    else:
        y = draw_line(c, "", "FEDERAL AMOUNT OWED", result.line_37_owed, y, bold=True)

    # IL summary
    y -= 5
    y = draw_section(c, "Illinois Tax Summary (IL-1040)", y)
    y = draw_line(c, "", "Illinois base income", result.il_line_7_base_income, y)
    y = draw_line(c, "", "Personal exemptions", result.il_line_9_exemptions, y)
    y = draw_line(c, "", "Illinois taxable income", result.il_line_10_taxable, y)
    y = draw_line(c, "", "Illinois tax (4.95%)", result.il_line_11_tax, y, bold=True)
    il_payments = result.il_line_18_withheld + result.il_estimated_payments
    y = draw_line(c, "", "State withholding + payments", il_payments, y)
    if result.il_refund > 0:
        y = draw_line(c, "", "ILLINOIS REFUND", result.il_refund, y, bold=True)
    else:
        y = draw_line(c, "", "ILLINOIS AMOUNT OWED", result.il_owed, y, bold=True)

    # Combined
    y -= 10
    y = draw_section(c, "Combined Bottom Line", y)
    net = result.line_34_overpaid + result.il_refund - result.line_37_owed - result.il_owed
    if net >= 0:
        y = draw_line(c, "", "NET TOTAL REFUND", net, y, bold=True)
    else:
        y = draw_line(c, "", "NET TOTAL OWED", abs(net), y, bold=True)

    # Rates
    y -= 15
    c.setFont("Helvetica", 8)
    if result.line_11_agi > 0:
        fed_eff = (result.line_24_total_tax / result.line_11_agi) * 100
        il_eff = (result.il_line_11_tax / result.line_11_agi) * 100
        c.drawString(58, y, f"Effective rates — Federal: {fed_eff:.1f}%  |  Illinois: {il_eff:.1f}%  |  Combined: {fed_eff + il_eff:.1f}%")

    y -= 15
    c.setFont("Helvetica-Bold", 8)
    c.setFillColor(colors.HexColor("#c62828"))
    c.drawString(58, y, "DRAFT — FOR REVIEW ONLY — NOT FOR FILING")
    c.setFillColor(colors.black)
    y -= 20
    c.setFont("Helvetica", 8)
    c.drawString(58, y, f"Forms generated: {', '.join(result.forms_generated)}")

    c.showPage()
    c.save()
    buf.seek(0)
    return buf


