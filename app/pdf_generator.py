# IL-1040 — Illinois Individual Income Tax Return
# ---------------------------------------------------------------------------
def generate_il1040(result: TaxResult) -> BytesIO:
    template = str(TEMPLATE_DIR / "il1040.pdf")
    f = {}
    filer = result.filer

    # Filer info
    f["step1-A-firstnamemi"] = f"{filer.first_name} {filer.last_name[0]}." if filer.last_name else filer.first_name
    f["step1-A-lastname"] = filer.last_name
    f["step1-A-ssn"] = filer.ssn
    f["step1-A-mailingaddress"] = filer.address_street
    f["step1-A-city"] = filer.address_city
    f["step1-A-state"] = filer.address_state
    f["step1-A-zip"] = filer.address_zip

    if result.spouse and result.filing_status == "mfj":
        f["step1-A-spousefirstnamemi"] = f"{result.spouse.first_name} {result.spouse.last_name[0]}."
        f["step1-A-spouselastname"] = result.spouse.last_name
        f["step1-A-spousessn"] = result.spouse.ssn

    # Income lines
    f["Federally adjusted income"] = _money(result.il_line_1_federal_agi)    # Line 1
    f["Other additions"] = _money(result.il_line_3_additions)                # Line 3
    f["Total income"] = _money(result.il_line_7_base_income)                 # Line 7
    f["Illinois base income"] = _money(result.il_line_7_base_income)         # Line 9

    # Exemptions
    num_ex = 1
    if result.filing_status == "mfj" and result.spouse:
        num_ex = 2
    num_ex += result.num_dependents
    f["Exemption allowance"] = _money(result.il_line_9_exemptions)           # Line 10
    f["Claiming dependents"] = str(num_ex)

    # Tax
    f["Illinois net income from Schedule NR"] = _money(result.il_line_10_taxable)  # Net income
    f["Income tax"] = _money(result.il_line_11_tax)                          # Line 14

    # Tax after credits
    f["Tax after nonrefundable credits"] = _money(result.il_line_11_tax)
    f["Total Tax"] = _money(result.il_line_11_tax)
    f["Total tax from Page 1"] = _money(result.il_line_11_tax)

    # Payments
    f["Illinois Income Tax withheld"] = _money(result.il_line_18_withheld)
    if result.il_estimated_payments > 0:
        f["Estimated payments"] = _money(result.il_estimated_payments)

    total_payments = result.il_line_18_withheld + result.il_estimated_payments
    f["Total payments and refundable credit"] = _money(total_payments)

    if result.il_refund > 0:
        f["If Line 31 is greater"] = _money(result.il_refund)
        f["Overpayment amount"] = _money(result.il_refund)
        f["Refunded to you"] = _money(result.il_refund)
    elif result.il_owed > 0:
        f["If Line 24 is greater"] = _money(result.il_owed)
        f["Amount you owe"] = _money(result.il_owed)

    return _fill_pdf(template, f)


def generate_generic_state_summary(state_result) -> BytesIO:
    """Generate a ReportLab summary page for states without official fillable PDFs."""
    buf = BytesIO()
    c = canvas.Canvas(buf, pagesize=letter)

    y = HEIGHT - 1 * inch
    c.setFont("Helvetica-Bold", 16)
    c.drawString(1 * inch, y, f"{state_result.state_name} State Tax Return")
    y -= 0.3 * inch
    c.setFont("Helvetica", 10)
    c.drawString(1 * inch, y, f"Form: {state_result.form_name or 'N/A'}  |  "
                               f"Return type: {state_result.return_type.title()}")
    y -= 0.5 * inch

    c.setFont("Helvetica-Bold", 12)
    c.drawString(1 * inch, y, "Income")
    y -= 0.25 * inch
    c.setFont("Helvetica", 10)
    lines = [
        ("Federal AGI", state_result.federal_agi),
        ("Additions", state_result.additions),
        ("Base Income", state_result.base_income),
    ]
    if state_result.return_type == "nonresident":
        lines.append(("Allocated Income", state_result.allocated_income))
        lines.append(("Allocation %", f"{state_result.allocation_pct:.1%}"))
    lines += [
        ("Exemptions", state_result.exemptions),
        ("Standard Deduction", state_result.standard_deduction_amount),
        ("Taxable Income", state_result.taxable_income),
    ]
    for label, val in lines:
        if isinstance(val, str):
            c.drawString(1.2 * inch, y, f"{label}:")
            c.drawRightString(5.5 * inch, y, val)
        else:
            c.drawString(1.2 * inch, y, f"{label}:")
            c.drawRightString(5.5 * inch, y, f"${val:,.2f}")
        y -= 0.2 * inch

    y -= 0.15 * inch
    c.setFont("Helvetica-Bold", 12)
    c.drawString(1 * inch, y, "Tax")
    y -= 0.25 * inch
    c.setFont("Helvetica", 10)
    tax_lines = [
        ("State Tax", state_result.tax),
        ("Surtax", state_result.surtax),
        ("Total Tax", state_result.total_tax),
    ]
    for label, val in tax_lines:
        c.drawString(1.2 * inch, y, f"{label}:")
        c.drawRightString(5.5 * inch, y, f"${val:,.2f}")
        y -= 0.2 * inch

    y -= 0.15 * inch
    c.setFont("Helvetica-Bold", 12)
    c.drawString(1 * inch, y, "Payments & Credits")
    y -= 0.25 * inch
    c.setFont("Helvetica", 10)
    pay_lines = [
        ("Withholding", state_result.withholding),
        ("Estimated Payments", state_result.estimated_payments),
        ("Credit for Other States", state_result.credit_for_other_states),
    ]
    for label, val in pay_lines:
        c.drawString(1.2 * inch, y, f"{label}:")
        c.drawRightString(5.5 * inch, y, f"${val:,.2f}")
        y -= 0.2 * inch

    y -= 0.15 * inch
    c.setFont("Helvetica-Bold", 12)
    c.drawString(1 * inch, y, "Bottom Line")
    y -= 0.25 * inch
    c.setFont("Helvetica", 10)
    if state_result.refund > 0:
        c.setFillColor(colors.darkgreen)
        c.drawString(1.2 * inch, y, "REFUND:")
        c.drawRightString(5.5 * inch, y, f"${state_result.refund:,.2f}")
    elif state_result.owed > 0:
        c.setFillColor(colors.red)
        c.drawString(1.2 * inch, y, "AMOUNT OWED:")
        c.drawRightString(5.5 * inch, y, f"${state_result.owed:,.2f}")
    else:
        c.drawString(1.2 * inch, y, "No tax due")

    c.setFillColor(colors.black)
    y -= 0.5 * inch
    c.setFont("Helvetica", 8)
    c.drawString(1 * inch, y, "Generated by TaxLens — for estimation purposes only. "
                               "File official state return via state tax authority.")

    c.save()
    buf.seek(0)
    return buf


