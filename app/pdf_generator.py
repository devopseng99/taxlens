# IL-1040 — Illinois Individual Income Tax Return
# ---------------------------------------------------------------------------
def generate_il1040(result: TaxResult) -> BytesIO:
    buf = BytesIO()
    c = canvas.Canvas(buf, pagesize=letter)

    y = HEIGHT - 40
    y = draw_header(c, "IL-1040", "Illinois Individual Income Tax Return", result.tax_year, y)
    y = draw_filer_info(c, result, y)

    y = draw_section(c, "Income", y)
    y = draw_line(c, "1", "Federal adjusted gross income (from 1040 Line 11)", result.il_line_1_federal_agi, y)
    y = draw_line(c, "3", "Additions to income", result.il_line_3_additions, y)
    y = draw_line(c, "7", "Illinois base income", result.il_line_7_base_income, y, bold=True)

    y = draw_section(c, "Exemptions", y)
    num_ex = 1
    if result.filing_status == "mfj" and result.spouse:
        num_ex = 2
    num_ex += result.num_dependents
    y = draw_line(c, "9", f"Exemptions ({num_ex} × $2,775)", result.il_line_9_exemptions, y)
    y = draw_line(c, "10", "Illinois taxable income", result.il_line_10_taxable, y, bold=True)

    y = draw_section(c, "Tax", y)
    y = draw_line(c, "11", f"Income tax (4.95% × {fmt(result.il_line_10_taxable)})", result.il_line_11_tax, y, bold=True)

    y = draw_section(c, "Payments and Credits", y)
    y = draw_line(c, "18", "Illinois income tax withheld (W-2s)", result.il_line_18_withheld, y)
    if result.il_estimated_payments > 0:
        y = draw_line(c, "19", "Estimated payments", result.il_estimated_payments, y)

    y -= 5
    il_total_payments = result.il_line_18_withheld + result.il_estimated_payments
    if result.il_refund > 0:
        y = draw_section(c, "Refund", y)
        y = draw_line(c, "20", "OVERPAYMENT (REFUND)", result.il_refund, y, bold=True)
    else:
        y = draw_section(c, "Amount You Owe", y)
        y = draw_line(c, "20", "AMOUNT YOU OWE", result.il_owed, y, bold=True)

    # Effective rate
    y -= 10
    c.setFont("Helvetica", 7)
    c.setFillColor(colors.gray)
    c.drawString(58, y, f"Illinois flat tax rate: 4.95%")
    c.setFillColor(colors.black)

    c.showPage()
    c.save()
    buf.seek(0)
    return buf


