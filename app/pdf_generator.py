def generate_all_pdfs(result: TaxResult, output_dir: str) -> dict:
    """Generate all tax form PDFs and save to output directory."""
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)
    paths = {}

    # Summary cover page (ReportLab branded)
    buf = generate_summary_page(result)
    p = out / "summary.pdf"
    p.write_bytes(buf.read())
    paths["summary"] = str(p)

    # Form 1040 (official IRS template)
    buf = generate_1040(result)
    p = out / "form_1040.pdf"
    p.write_bytes(buf.read())
    paths["1040"] = str(p)

    # Schedule C (if business income)
    if result.sched_c_businesses:
        buf = generate_schedule_c(result)
        p = out / "schedule_c.pdf"
        p.write_bytes(buf.read())
        paths["schedule_c"] = str(p)

    # Schedule SE (if self-employment tax)
    if result.se_tax > 0:
        buf = generate_schedule_se(result)
        p = out / "schedule_se.pdf"
        p.write_bytes(buf.read())
        paths["schedule_se"] = str(p)

    # Schedule A (if itemizing or for reference)
    buf = generate_schedule_a(result)
    p = out / "schedule_a.pdf"
    p.write_bytes(buf.read())
    paths["schedule_a"] = str(p)

    # Schedule B (if interest/dividends)
    if result.sched_b_interest_total > 0 or result.sched_b_dividends_total > 0:
        buf = generate_schedule_b(result)
        p = out / "schedule_b.pdf"
        p.write_bytes(buf.read())
        paths["schedule_b"] = str(p)

    # Schedule D (if capital transactions)
    if result.capital_transactions:
        buf = generate_schedule_d(result)
        p = out / "schedule_d.pdf"
        p.write_bytes(buf.read())
        paths["schedule_d"] = str(p)

    # Schedule 1 (if any additional income or adjustments)
    has_sched1_income = (result.sched_c_total_profit != 0 or result.sched_e_net_income != 0
                         or result.unemployment_compensation > 0 or result.alimony_received > 0
                         or result.gambling_winnings > 0 or result.k1_ordinary_income > 0)
    has_sched1_adj = (result.se_tax_deduction > 0 or result.hsa_deduction > 0
                      or result.ira_deduction > 0 or result.student_loan_deduction > 0
                      or result.educator_expense_deduction > 0 or result.alimony_paid > 0)
    if has_sched1_income or has_sched1_adj:
        buf = generate_schedule_1(result)
        p = out / "schedule_1.pdf"
        p.write_bytes(buf.read())
        paths["schedule_1"] = str(p)

    # Schedule 3 (if any additional credits)
    has_sched3 = (result.foreign_tax_credit > 0 or result.cdcc > 0
                  or result.education_credit > 0 or result.education_credit_refundable > 0
                  or result.savers_credit > 0 or result.energy_total_credit > 0
                  or result.eitc > 0)
    if has_sched3:
        buf = generate_schedule_3(result)
        p = out / "schedule_3.pdf"
        p.write_bytes(buf.read())
        paths["schedule_3"] = str(p)

    # Schedule E (if rental properties)
    if result.sched_e_properties:
        buf = generate_schedule_e(result)
        p = out / "schedule_e.pdf"
        p.write_bytes(buf.read())
        paths["schedule_e"] = str(p)

    # Schedule 2 + Form 8959 + Form 8960 (if surtaxes apply)
    if result.niit > 0 or result.additional_medicare_tax > 0 or result.se_tax > 0:
        buf = generate_schedule_2(result)
        p = out / "schedule_2.pdf"
        p.write_bytes(buf.read())
        paths["schedule_2"] = str(p)

    if result.additional_medicare_tax > 0:
        buf = generate_form_8959(result)
        p = out / "form_8959.pdf"
        p.write_bytes(buf.read())
        paths["form_8959"] = str(p)

    if result.niit > 0:
        buf = generate_form_8960(result)
        p = out / "form_8960.pdf"
        p.write_bytes(buf.read())
        paths["form_8960"] = str(p)

    # Form 6251 — AMT (if AMT applies)
    if result.amt > 0:
        buf = generate_form_6251(result)
        p = out / "form_6251.pdf"
        p.write_bytes(buf.read())
        paths["form_6251"] = str(p)

    # Form 8863 — Education Credits (if any education credit)
    if result.education_credit > 0 or result.education_credit_refundable > 0:
        buf = generate_form_8863(result)
        p = out / "form_8863.pdf"
        p.write_bytes(buf.read())
        paths["form_8863"] = str(p)

    # Schedule EIC — EITC (if earned income credit)
    if result.eitc > 0:
        buf = generate_schedule_eic(result)
        p = out / "schedule_eic.pdf"
        p.write_bytes(buf.read())
        paths["schedule_eic"] = str(p)

    # Form 2441 — CDCC (if dependent care credit)
    if result.cdcc > 0:
        buf = generate_form_2441(result)
        p = out / "form_2441.pdf"
        p.write_bytes(buf.read())
        paths["form_2441"] = str(p)

    # Form 8880 — Saver's Credit (if retirement savings credit)
    if result.savers_credit > 0:
        buf = generate_form_8880(result)
        p = out / "form_8880.pdf"
        p.write_bytes(buf.read())
        paths["form_8880"] = str(p)

    # Form 8889 — HSA (if HSA contributions)
    if result.hsa_deduction > 0 or result.form_8889_contributions > 0:
        buf = generate_form_8889(result)
        p = out / "form_8889.pdf"
        p.write_bytes(buf.read())
        paths["form_8889"] = str(p)

    # Form 8606 — Nondeductible IRAs (if basis tracking)
    if result.form_8606_nondeductible_contribution > 0 or result.form_8606_prior_year_basis > 0:
        buf = generate_form_8606(result)
        p = out / "form_8606.pdf"
        p.write_bytes(buf.read())
        paths["form_8606"] = str(p)

    # Form 8949 — Digital Asset Transactions (if crypto trades)
    if result.crypto_transactions_count > 0:
        buf = generate_form_8949(result)
        p = out / "form_8949.pdf"
        p.write_bytes(buf.read())
        paths["form_8949"] = str(p)

    # Form 5695 — Residential Energy Credits (if energy credit claimed)
    if result.energy_total_credit > 0:
        buf = generate_form_5695(result)
        p = out / "form_5695.pdf"
        p.write_bytes(buf.read())
        paths["form_5695"] = str(p)

    # K-1 Summary (if passthrough income)
    if result.k1_ordinary_income > 0 or result.k1_guaranteed_payments > 0 or result.k1_capital_gains != 0:
        buf = generate_k1_summary(result)
        p = out / "k1_summary.pdf"
        p.write_bytes(buf.read())
        paths["k1_summary"] = str(p)

    # Social Security Summary (if SS benefits)
    if result.ss_gross_benefits > 0:
        buf = generate_ss_summary(result)
        p = out / "ss_summary.pdf"
        p.write_bytes(buf.read())
        paths["ss_summary"] = str(p)

    # Retirement Summary — 1099-R / IRA (if retirement distributions)
    if result.retirement_distributions_count > 0:
        buf = generate_retirement_summary(result)
        p = out / "retirement_summary.pdf"
        p.write_bytes(buf.read())
        paths["retirement_summary"] = str(p)

        # Individual 1099-R PDFs
        if hasattr(result, "_retirement_distributions"):
            for i, dist in enumerate(result._retirement_distributions):
                buf = generate_1099r(dist)
                fname = f"1099r_{i+1}.pdf" if i > 0 else "1099r.pdf"
                p = out / fname
                p.write_bytes(buf.read())
                paths[f"1099r{'_' + str(i+1) if i > 0 else ''}"] = str(p)

    # Form 4562 — Depreciation (if depreciable assets)
    if result.depreciation_assets_count > 0:
        buf = generate_form_4562(result)
        p = out / "form_4562.pdf"
        p.write_bytes(buf.read())
        paths["form_4562"] = str(p)

    # Form 2210 — Estimated Tax Penalty (if penalty assessed)
    if result.estimated_tax_penalty > 0:
        buf = generate_form_2210(result)
        p = out / "form_2210.pdf"
        p.write_bytes(buf.read())
        paths["form_2210"] = str(p)

    # Schedule AI — Annualized Installment Method (if used)
    if result.sched_ai_used:
        buf = generate_schedule_ai(result)
        p = out / "schedule_ai.pdf"
        p.write_bytes(buf.read())
        paths["schedule_ai"] = str(p)

    # Form 1040-ES — Estimated Tax Vouchers (if quarterly estimated tax > 0)
    if result.quarterly_estimated_tax > 0:
        buf = generate_1040es_vouchers(result)
        p = out / "form_1040es.pdf"
        p.write_bytes(buf.read())
        paths["1040es"] = str(p)

    # State returns — dispatch by state
    for sr in getattr(result, 'state_returns', []):
        if sr.state_code == "IL":
            # IL has official fillable template
            buf = generate_il1040(result)
            p = out / "il_1040.pdf"
            p.write_bytes(buf.read())
            paths["il_1040"] = str(p)
        elif sr.total_tax > 0 or sr.withholding > 0:
            # Generic ReportLab summary for other states
            buf = generate_generic_state_summary(sr)
            slug = sr.state_code.lower()
            fname = f"state_{slug}_{sr.return_type}.pdf"
            p = out / fname
            p.write_bytes(buf.read())
            paths[f"state_{slug}_{sr.return_type}"] = str(p)

    # Fallback: if no state_returns but IL fields populated (legacy path)
    if not getattr(result, 'state_returns', []) and result.il_line_11_tax > 0:
        buf = generate_il1040(result)
        p = out / "il_1040.pdf"
        p.write_bytes(buf.read())
        paths["il_1040"] = str(p)

    return paths
