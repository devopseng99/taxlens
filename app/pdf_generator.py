# Schedule A — Itemized Deductions
# ---------------------------------------------------------------------------
def generate_schedule_a(result: TaxResult) -> BytesIO:
    template = str(TEMPLATE_DIR / "f1040sa.pdf")
    f = {}
    p1 = "form1[0].Page1[0]."
    filer = result.filer

    f[p1 + "f1_1[0]"] = f"{filer.first_name} {filer.last_name}"
    f[p1 + "f1_2[0]"] = filer.ssn

    # Medical (lines 1-4)
    if result.sched_a_medical > 0:
        raw_medical = result.sched_a_medical + result.line_11_agi * 0.075
        f[p1 + "f1_3[0]"] = _money(raw_medical)                     # Line 1
        f[p1 + "f1_5[0]"] = _money(result.line_11_agi)              # Line 2 AGI
        f[p1 + "f1_6[0]"] = _money(result.line_11_agi * 0.075)      # Line 3
        f[p1 + "f1_7[0]"] = _money(result.sched_a_medical)          # Line 4

    # SALT (lines 5-7)
    f[p1 + "f1_11[0]"] = _money(result.sched_a_salt)                # Line 5d/7

    # Mortgage interest (line 8a)
    f[p1 + "f1_13[0]"] = _money(result.sched_a_mortgage_interest)   # Line 8a
    f[p1 + "Line8b_ReadOrder[0].f1_16[0]"] = _money(result.sched_a_mortgage_interest)  # Line 10

    # Charitable (lines 12-14)
    f[p1 + "f1_19[0]"] = _money(result.sched_a_charitable)          # Line 12
    f[p1 + "f1_22[0]"] = _money(result.sched_a_charitable)          # Line 14

    # Total (line 17)
    f[p1 + "f1_27[0]"] = _money(result.sched_a_total)               # Line 17

    return _fill_pdf(template, f)


