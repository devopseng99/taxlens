# Form 1040 — Official IRS fillable PDF
# ---------------------------------------------------------------------------
def generate_1040(result: TaxResult) -> BytesIO:
    template = str(TEMPLATE_DIR / "f1040.pdf")
    f = {}
    filer = result.filer

    # Page 1 — Personal info
    p1 = "topmostSubform[0].Page1[0]."
    f[p1 + "f1_04[0]"] = f"{filer.first_name} {filer.last_name[0]}." if filer.last_name else filer.first_name
    f[p1 + "f1_05[0]"] = filer.last_name
    f[p1 + "f1_06[0]"] = filer.ssn

    if result.spouse and result.filing_status == "mfj":
        f[p1 + "f1_07[0]"] = f"{result.spouse.first_name} {result.spouse.last_name[0]}."
        f[p1 + "f1_08[0]"] = result.spouse.last_name
        f[p1 + "f1_09[0]"] = result.spouse.ssn

    # Address
    addr = p1 + "Address_ReadOrder[0]."
    f[addr + "f1_10[0]"] = filer.address_street
    f[addr + "f1_12[0]"] = filer.address_city
    f[addr + "f1_13[0]"] = filer.address_state
    f[addr + "f1_14[0]"] = filer.address_zip

    # Filing status checkboxes
    fs = p1 + "FilingStatus_ReadOrder[0]."
    if result.filing_status == "single":
        f[fs + "c1_3[0]"] = "/1"
    elif result.filing_status == "mfj":
        f[fs + "c1_3[1]"] = "/1"
    elif result.filing_status == "mfs":
        f[fs + "c1_3[2]"] = "/1"
    if result.filing_status == "hoh":
        f[p1 + "c1_3[0]"] = "/1"

    # Income lines
    f[p1 + "f1_32[0]"] = _money(result.line_1a_wages)             # Line 1a wages
    f[p1 + "f1_42[0]"] = _money(result.line_2a_tax_exempt_interest)  # Line 2a
    f[p1 + "f1_43[0]"] = _money(result.line_2b_taxable_interest)    # Line 2b
    f[p1 + "f1_44[0]"] = _money(result.line_3a_qualified_dividends) # Line 3a
    f[p1 + "f1_45[0]"] = _money(result.line_3b_ordinary_dividends)  # Line 3b

    # Line 4a-11 block
    l = p1 + "Line4a-11_ReadOrder[0]."
    f[l + "f1_52[0]"] = _money(result.line_7_capital_gain_loss)     # Line 7
    f[l + "f1_53[0]"] = _money(result.line_8_other_income + result.line_8a_business_income)  # Line 8
    f[l + "f1_54[0]"] = _money(result.line_9_total_income)          # Line 9
    f[l + "f1_55[0]"] = _money(result.line_10_adjustments)          # Line 10
    f[l + "f1_56[0]"] = _money(result.line_11_agi)                  # Line 11

    # Deductions
    f[p1 + "f1_57[0]"] = _money(result.line_13_deduction)           # Line 12/13
    f[p1 + "f1_58[0]"] = _money(result.qbi_deduction)               # Line 13 QBI
    total_deductions = result.line_13_deduction + result.qbi_deduction
    f[p1 + "f1_59[0]"] = _money(total_deductions)                   # Line 14
    f[p1 + "f1_60[0]"] = _money(result.line_15_taxable_income)      # Line 15

    # Page 2
    p2 = "topmostSubform[0].Page2[0]."
    f[p2 + "f2_02[0]"] = _money(result.line_16_tax + result.capital_gains_tax)  # Line 16
    # Schedule 2 taxes (NIIT + Additional Medicare) go on line 23
    sched2_tax = result.niit + result.additional_medicare_tax
    if sched2_tax > 0:
        f[p2 + "f2_09[0]"] = _money(sched2_tax)                     # Line 23
    f[p2 + "f2_10[0]"] = _money(result.line_24_total_tax)           # Line 24

    f[p2 + "f2_11[0]"] = _money(result.line_25_federal_withheld)    # Line 25a
    f[p2 + "f2_14[0]"] = _money(result.line_25_federal_withheld)    # Line 25d total
    if result.estimated_payments > 0:
        f[p2 + "f2_15[0]"] = _money(result.estimated_payments)      # Line 26
    if result.line_27_ctc > 0:
        f[p2 + "f2_05[0]"] = _money(result.line_27_ctc)             # Line 19 CTC
    f[p2 + "f2_22[0]"] = _money(result.line_33_total_payments)      # Line 33

    if result.line_34_overpaid > 0:
        f[p2 + "f2_23[0]"] = _money(result.line_34_overpaid)        # Line 34
        f[p2 + "f2_24[0]"] = _money(result.line_34_overpaid)        # Line 35a
    if result.line_37_owed > 0:
        f[p2 + "f2_28[0]"] = _money(result.line_37_owed)            # Line 37

    return _fill_pdf(template, f)


