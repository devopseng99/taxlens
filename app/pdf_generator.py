# Schedule C — Profit or Loss from Business
# ---------------------------------------------------------------------------
def generate_schedule_c(result: TaxResult) -> BytesIO:
    template = str(TEMPLATE_DIR / "f1040sc.pdf")
    f = {}
    p1 = "topmostSubform[0].Page1[0]."
    filer = result.filer

    f[p1 + "f1_1[0]"] = f"{filer.first_name} {filer.last_name}"
    f[p1 + "f1_2[0]"] = filer.ssn

    # Use the first business for the main form
    biz_list = result.businesses or []
    if biz_list:
        biz = biz_list[0]
        f[p1 + "f1_3[0]"] = biz.business_name                      # Line A
        f[p1 + "BComb[0].f1_4[0]"] = biz.business_type              # Line B
        if biz.ein:
            f[p1 + "DComb[0].f1_6[0]"] = biz.ein                    # Line D

    # Use totals across all businesses
    total_gross = sum(b.gross_receipts for b in biz_list)
    total_cogs = sum(b.cost_of_goods_sold for b in biz_list)
    total_gross_profit = sum(b.gross_profit for b in biz_list)
    total_expenses = sum(b.total_expenses for b in biz_list)
    total_net = sum(b.net_profit for b in biz_list)

    f[p1 + "f1_8[0]"] = _money(total_gross)                         # Line 1 gross receipts
    f[p1 + "f1_11[0]"] = _money(total_gross)                        # Line 3
    f[p1 + "f1_12[0]"] = _money(total_cogs)                         # Line 4 COGS
    f[p1 + "f1_13[0]"] = _money(total_gross_profit)                  # Line 5 gross profit
    f[p1 + "f1_14[0]"] = _money(total_gross_profit)                  # Line 7

    # Expenses (lines 8-27)
    lines = p1 + "Lines8-17[0]."
    total_adv = sum(b.advertising for b in biz_list)
    total_car = sum(b.car_expenses for b in biz_list)
    total_ins = sum(b.insurance for b in biz_list)
    total_office = sum(b.office_expense for b in biz_list)
    total_rent = sum(b.rent for b in biz_list)
    total_supplies = sum(b.supplies for b in biz_list)
    total_util = sum(b.utilities for b in biz_list)
    total_other = sum(b.other_expenses for b in biz_list)

    f[lines + "f1_17[0]"] = _money(total_adv)                       # Line 8  advertising
    f[lines + "f1_18[0]"] = _money(total_car)                       # Line 9  car
    f[lines + "f1_22[0]"] = _money(total_ins)                       # Line 15 insurance
    f[lines + "f1_25[0]"] = _money(total_office)                    # Line 18 office
    lines2 = p1 + "Lines18-27[0]."
    f[lines2 + "f1_29[0]"] = _money(total_rent)                     # Line 20b rent
    f[lines2 + "f1_32[0]"] = _money(total_supplies)                 # Line 22 supplies
    f[lines2 + "f1_36[0]"] = _money(total_util)                     # Line 25 utilities
    f[lines2 + "f1_38[0]"] = _money(total_other)                    # Line 27a other

    # Home office deduction (Line 30)
    total_home_office = 0.0
    for b in biz_list:
        if b.home_total_sqft > 0 and b.home_office_sqft > 0:
            total_home_office += b.home_expenses * (b.home_office_sqft / b.home_total_sqft)
    if total_home_office > 0:
        f[p1 + "f1_40[0]"] = _money(total_home_office)              # Line 30 home office

    # Totals
    f[p1 + "f1_41[0]"] = _money(total_expenses)                     # Line 28
    f[p1 + "f1_42[0]"] = _money(total_net)                          # Line 31 net profit

    # If multiple businesses, add note
    if len(biz_list) > 1:
        desc_parts = [f"{b.business_name}: ${b.net_profit:,.0f}" for b in biz_list]
        f[lines2 + "f1_39[0]"] = "; ".join(desc_parts)              # Line 27b description

    return _fill_pdf(template, f)


