def generate_schedule_b(result: TaxResult) -> BytesIO:
    template = str(TEMPLATE_DIR / "f1040sb.pdf")
    f = {}
    p1 = "topmostSubform[0].Page1[0]."
    filer = result.filer

    f[p1 + "f1_01[0]"] = f"{filer.first_name} {filer.last_name}"
    f[p1 + "f1_02[0]"] = filer.ssn

    # Interest - Line 1 (payer + amount)
    if result.sched_b_interest_total > 0:
        f[p1 + "Line1_ReadOrder[0].f1_03[0]"] = "Various (from 1099-INT/OCR)"
        f[p1 + "f1_04[0]"] = _money(result.sched_b_interest_total)
    # Line 4 total interest
    f[p1 + "f1_31[0]"] = _money(result.sched_b_interest_total)
    f[p1 + "f1_33[0]"] = _money(result.sched_b_interest_total)

    # Dividends - Part II
    if result.sched_b_dividends_total > 0:
        f[p1 + "f1_35[0]"] = "Various payers"
        f[p1 + "f1_36[0]"] = _money(result.sched_b_dividends_total)
    # Line 6 total dividends
    f[p1 + "f1_63[0]"] = _money(result.sched_b_dividends_total) if hasattr(result, 'sched_b_dividends_total') else ""

    return _fill_pdf(template, f)


