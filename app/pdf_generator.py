"""PDF tax form generator using ReportLab — Forms 1040, Schedules A/B/D, IL-1040."""

from io import BytesIO
from reportlab.lib.pagesizes import letter
from reportlab.lib.units import inch
from reportlab.pdfgen import canvas
from reportlab.lib import colors

from tax_engine import TaxResult

WIDTH, HEIGHT = letter  # 612 x 792


# ---------------------------------------------------------------------------
# Formatting helpers
# ---------------------------------------------------------------------------
def fmt(amount: float) -> str:
    """Format dollar amount."""
    if amount == 0:
        return "—"
    if amount < 0:
        return f"({abs(amount):,.2f})"
    return f"{amount:,.2f}"


def draw_header(c: canvas.Canvas, form_name: str, form_title: str, tax_year: int, y: float) -> float:
    """Draw form header. Returns new y position."""
    # Top border
    c.setStrokeColor(colors.HexColor("#1a237e"))
    c.setLineWidth(2)
    c.line(50, y, WIDTH - 50, y)
    y -= 5

    # Form number (left)
    c.setFont("Helvetica-Bold", 18)
    c.setFillColor(colors.HexColor("#1a237e"))
    c.drawString(55, y - 20, form_name)

    # Title (center)
    c.setFont("Helvetica-Bold", 11)
    c.drawCentredString(WIDTH / 2, y - 18, form_title)

    # Tax year (right)
    c.setFont("Helvetica-Bold", 14)
    c.drawRightString(WIDTH - 55, y - 20, str(tax_year))

    c.setFont("Helvetica", 7)
    c.setFillColor(colors.gray)
    c.drawCentredString(WIDTH / 2, y - 30, "Department of the Treasury — Internal Revenue Service")
    c.drawCentredString(WIDTH / 2, y - 39, "DRAFT — Generated by TaxLens for review purposes only")

    y -= 45
    c.setStrokeColor(colors.HexColor("#1a237e"))
    c.setLineWidth(1)
    c.line(50, y, WIDTH - 50, y)
    c.setFillColor(colors.black)

    return y - 10


def draw_section(c: canvas.Canvas, title: str, y: float) -> float:
    """Draw a section header."""
    c.setFont("Helvetica-Bold", 9)
    c.setFillColor(colors.HexColor("#1a237e"))
    c.drawString(55, y, title)
    c.setLineWidth(0.5)
    c.setStrokeColor(colors.HexColor("#bbdefb"))
    c.line(55, y - 3, WIDTH - 55, y - 3)
    c.setFillColor(colors.black)
    return y - 16


def draw_line(c: canvas.Canvas, line_num: str, description: str, amount: float, y: float,
              bold: bool = False, indent: int = 0) -> float:
    """Draw a single form line with number, description, and amount."""
    font = "Helvetica-Bold" if bold else "Helvetica"
    c.setFont(font, 8)

    x_num = 58 + indent
    x_desc = 80 + indent
    x_amt = WIDTH - 60

    if line_num:
        c.setFillColor(colors.HexColor("#1a237e"))
        c.drawString(x_num, y, line_num)
    c.setFillColor(colors.black)
    c.drawString(x_desc, y, description)

    # Amount box
    if bold:
        c.setStrokeColor(colors.HexColor("#1a237e"))
        c.setFillColor(colors.HexColor("#e8eaf6"))
        c.rect(x_amt - 80, y - 3, 130, 13, fill=1, stroke=1)
        c.setFillColor(colors.HexColor("#1a237e"))
    else:
        c.setStrokeColor(colors.HexColor("#e0e0e0"))
        c.rect(x_amt - 80, y - 3, 130, 13, fill=0, stroke=1)
        c.setFillColor(colors.black)

    c.setFont(font, 8)
    c.drawRightString(x_amt + 45, y, fmt(amount))
    c.setFillColor(colors.black)

    return y - 16


def draw_info_row(c: canvas.Canvas, label: str, value: str, y: float, x: float = 58) -> float:
    c.setFont("Helvetica", 7)
    c.setFillColor(colors.gray)
    c.drawString(x, y, label)
    c.setFillColor(colors.black)
    c.setFont("Helvetica", 9)
    c.drawString(x + 80, y, value)
    return y - 14


def draw_filer_info(c: canvas.Canvas, result: TaxResult, y: float) -> float:
    """Draw filer information block."""
    y = draw_section(c, "Filing Information", y)
    filer = result.filer

    status_labels = {"single": "Single", "mfj": "Married Filing Jointly",
                     "hoh": "Head of Household", "mfs": "Married Filing Separately"}

    y = draw_info_row(c, "Name:", f"{filer.first_name} {filer.last_name}", y)
    y = draw_info_row(c, "SSN:", filer.ssn, y)

    if result.filing_status == "mfj" and result.spouse:
        y = draw_info_row(c, "Spouse:", f"{result.spouse.first_name} {result.spouse.last_name}", y)
        y = draw_info_row(c, "Spouse SSN:", result.spouse.ssn, y)

    y = draw_info_row(c, "Address:", filer.address_street, y)
    y = draw_info_row(c, "City/St/Zip:",
                      f"{filer.address_city}, {filer.address_state} {filer.address_zip}", y)
    y = draw_info_row(c, "Filing Status:", status_labels.get(result.filing_status, result.filing_status), y)

    if result.num_dependents > 0:
        y = draw_info_row(c, "Dependents:", str(result.num_dependents), y)

    return y - 5


# ---------------------------------------------------------------------------
# Form 1040
# ---------------------------------------------------------------------------
def generate_1040(result: TaxResult) -> BytesIO:
    """Generate Form 1040 PDF."""
    buf = BytesIO()
    c = canvas.Canvas(buf, pagesize=letter)

    y = HEIGHT - 40
    y = draw_header(c, "1040", "U.S. Individual Income Tax Return", result.tax_year, y)
    y = draw_filer_info(c, result, y)

    # --- Income ---
    y = draw_section(c, "Income", y)
    y = draw_line(c, "1a", "Wages, salaries, tips (from W-2s)", result.line_1a_wages, y)

    # W-2 detail
    for i, w in enumerate(result.w2s):
        c.setFont("Helvetica", 7)
        c.setFillColor(colors.gray)
        c.drawString(100, y + 4, f"W-2 #{i+1}: {w.employer_name} (EIN: {w.employer_ein}) — ${w.wages:,.2f}")
        c.setFillColor(colors.black)
        y -= 12

    y = draw_line(c, "2b", "Taxable interest", result.line_2b_taxable_interest, y)
    y = draw_line(c, "3a", "Qualified dividends", result.line_3a_qualified_dividends, y)
    y = draw_line(c, "3b", "Ordinary dividends", result.line_3b_ordinary_dividends, y)
    y = draw_line(c, "7", "Capital gain or (loss) — from Schedule D", result.line_7_capital_gain_loss, y)
    if result.line_8a_business_income != 0:
        y = draw_line(c, "8a", "Business income or (loss) — from Schedule C", result.line_8a_business_income, y)
    y = draw_line(c, "8", "Other income", result.line_8_other_income, y)
    y = draw_line(c, "9", "Total income", result.line_9_total_income, y, bold=True)

    # --- Adjustments ---
    y = draw_section(c, "Adjustments to Income", y)
    if result.se_tax_deduction > 0:
        y = draw_line(c, "", "Deductible part of self-employment tax", result.se_tax_deduction, y)
    y = draw_line(c, "10", "Total adjustments", result.line_10_adjustments, y)
    y = draw_line(c, "11", "Adjusted gross income (AGI)", result.line_11_agi, y, bold=True)

    # --- Deductions ---
    y = draw_section(c, "Deductions", y)
    dtype = f"{'Standard' if result.deduction_type == 'standard' else 'Itemized'} deduction"
    y = draw_line(c, "12", f"{dtype} ({result.deduction_type})", result.line_13_deduction, y)
    if result.qbi_deduction > 0:
        y = draw_line(c, "13", "Qualified business income deduction (Section 199A)", result.qbi_deduction, y)
    y = draw_line(c, "15", "Taxable income", result.line_15_taxable_income, y, bold=True)

    # --- Tax ---
    y = draw_section(c, "Tax and Credits", y)
    y = draw_line(c, "16", "Tax (from tax table / brackets)", result.line_16_tax, y)
    if result.capital_gains_tax > 0:
        y = draw_line(c, "", "Capital gains tax (preferential rates)", result.capital_gains_tax, y)
    if result.se_tax > 0:
        y = draw_line(c, "", "Self-employment tax (Schedule SE)", result.se_tax, y)
    if result.line_27_ctc > 0:
        y = draw_line(c, "27", "Child tax credit", -result.line_27_ctc, y)
    y = draw_line(c, "24", "Total tax", result.line_24_total_tax, y, bold=True)

    # --- Payments ---
    y = draw_section(c, "Payments", y)
    y = draw_line(c, "25a", "Federal income tax withheld (W-2s)", result.line_25_federal_withheld, y)
    if result.estimated_payments > 0:
        y = draw_line(c, "26", "Estimated tax payments", result.estimated_payments, y)
    y = draw_line(c, "33", "Total payments", result.line_33_total_payments, y, bold=True)

    # --- Refund / Owed ---
    y -= 5
    if result.line_34_overpaid > 0:
        y = draw_section(c, "Refund", y)
        y = draw_line(c, "34", "AMOUNT OVERPAID (REFUND)", result.line_34_overpaid, y, bold=True)
    else:
        y = draw_section(c, "Amount You Owe", y)
        y = draw_line(c, "37", "AMOUNT YOU OWE", result.line_37_owed, y, bold=True)

    # Effective rate
    y -= 10
    c.setFont("Helvetica", 7)
    c.setFillColor(colors.gray)
    if result.line_11_agi > 0:
        eff_rate = (result.line_24_total_tax / result.line_11_agi) * 100
        c.drawString(58, y, f"Effective federal tax rate: {eff_rate:.1f}%")
    c.setFillColor(colors.black)

    c.showPage()
    c.save()
    buf.seek(0)
    return buf


