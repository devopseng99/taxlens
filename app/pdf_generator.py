# Combined summary cover page
# ---------------------------------------------------------------------------
def generate_summary_page(result: TaxResult) -> BytesIO:
    buf = BytesIO()
    c = canvas.Canvas(buf, pagesize=letter)

    y = HEIGHT - 40
    y = draw_header(c, "TaxLens", "Tax Return Summary", result.tax_year, y)
    y = draw_filer_info(c, result, y)

    # Federal summary
    y = draw_section(c, "Federal Tax Summary (Form 1040)", y)
    y = draw_line(c, "", "Total income", result.line_9_total_income, y)
    if result.sched_c_total_profit > 0:
        y = draw_line(c, "", "  Business income (Schedule C)", result.sched_c_total_profit, y)
    y = draw_line(c, "", "Adjusted gross income", result.line_11_agi, y)
    y = draw_line(c, "", f"Deduction ({result.deduction_type})", result.line_13_deduction, y)
    if result.qbi_deduction > 0:
        y = draw_line(c, "", "  QBI deduction (Section 199A)", result.qbi_deduction, y)
    y = draw_line(c, "", "Taxable income", result.line_15_taxable_income, y)
    if result.se_tax > 0:
        y = draw_line(c, "", "  Self-employment tax", result.se_tax, y)
    y = draw_line(c, "", "Total federal tax", result.line_24_total_tax, y, bold=True)
    y = draw_line(c, "", "Total withholding + payments", result.line_33_total_payments, y)
    if result.line_34_overpaid > 0:
        y = draw_line(c, "", "FEDERAL REFUND", result.line_34_overpaid, y, bold=True)
    else:
        y = draw_line(c, "", "FEDERAL AMOUNT OWED", result.line_37_owed, y, bold=True)

    # Illinois summary
    y -= 5
    y = draw_section(c, "Illinois Tax Summary (IL-1040)", y)
    y = draw_line(c, "", "Illinois base income", result.il_line_7_base_income, y)
    y = draw_line(c, "", "Personal exemptions", result.il_line_9_exemptions, y)
    y = draw_line(c, "", "Illinois taxable income", result.il_line_10_taxable, y)
    y = draw_line(c, "", "Illinois tax (4.95%)", result.il_line_11_tax, y, bold=True)
    y = draw_line(c, "", "State withholding + payments", result.il_line_18_withheld + result.il_estimated_payments, y)
    if result.il_refund > 0:
        y = draw_line(c, "", "ILLINOIS REFUND", result.il_refund, y, bold=True)
    else:
        y = draw_line(c, "", "ILLINOIS AMOUNT OWED", result.il_owed, y, bold=True)

    # Combined bottom line
    y -= 10
    y = draw_section(c, "Combined Bottom Line", y)
    net = result.line_34_overpaid + result.il_refund - result.line_37_owed - result.il_owed
    if net >= 0:
        y = draw_line(c, "", "NET TOTAL REFUND", net, y, bold=True)
    else:
        y = draw_line(c, "", "NET TOTAL OWED", abs(net), y, bold=True)

    # Effective rates
    y -= 15
    c.setFont("Helvetica", 8)
    if result.line_11_agi > 0:
        fed_eff = (result.line_24_total_tax / result.line_11_agi) * 100
        il_eff = (result.il_line_11_tax / result.line_11_agi) * 100
        combined = fed_eff + il_eff
        c.drawString(58, y, f"Effective rates — Federal: {fed_eff:.1f}%  |  Illinois: {il_eff:.1f}%  |  Combined: {combined:.1f}%")

    y -= 15
    c.setFont("Helvetica-Bold", 8)
    c.setFillColor(colors.HexColor("#c62828"))
    c.drawString(58, y, "DRAFT — FOR REVIEW ONLY — NOT FOR FILING")
    c.setFillColor(colors.black)

    # Forms included
    y -= 20
    c.setFont("Helvetica", 8)
    c.drawString(58, y, f"Forms generated: {', '.join(result.forms_generated)}")

    c.showPage()
    c.save()
    buf.seek(0)
    return buf


# ---------------------------------------------------------------------------
# Public API — generate all forms
# ---------------------------------------------------------------------------
def generate_all_pdfs(result: TaxResult, output_dir: str) -> dict:
    """Generate all tax form PDFs and save to output directory.

    Returns dict of form_name -> file_path.
    """
    from pathlib import Path
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    paths = {}

    # Summary cover page
    buf = generate_summary_page(result)
    p = out / "summary.pdf"
    p.write_bytes(buf.read())
    paths["summary"] = str(p)

    # Form 1040
    buf = generate_1040(result)
    p = out / "form_1040.pdf"
    p.write_bytes(buf.read())
    paths["1040"] = str(p)

    # Schedule C (if business income)
    if result.sched_c_businesses:
        buf = generate_schedule_c(result)
        p = out / "schedule_c.pdf"
        p.write_bytes(buf.read())
        paths["schedule_c"] = str(p)

    # Schedule SE (if self-employment tax)
    if result.se_tax > 0:
        buf = generate_schedule_se(result)
        p = out / "schedule_se.pdf"
        p.write_bytes(buf.read())
        paths["schedule_se"] = str(p)

    # Schedule A (if itemizing or always for reference)
    buf = generate_schedule_a(result)
    p = out / "schedule_a.pdf"
    p.write_bytes(buf.read())
    paths["schedule_a"] = str(p)

    # Schedule B (if significant interest/dividends)
    if result.sched_b_interest_total > 0 or result.sched_b_dividends_total > 0:
        buf = generate_schedule_b(result)
        p = out / "schedule_b.pdf"
        p.write_bytes(buf.read())
        paths["schedule_b"] = str(p)

    # Schedule D (if capital transactions)
    if result.capital_transactions:
        buf = generate_schedule_d(result)
        p = out / "schedule_d.pdf"
        p.write_bytes(buf.read())
        paths["schedule_d"] = str(p)

    # IL-1040
    buf = generate_il1040(result)
    p = out / "il_1040.pdf"
    p.write_bytes(buf.read())
    paths["il_1040"] = str(p)

    return paths
