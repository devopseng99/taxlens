# ---------------------------------------------------------------------------
# Public API — generate all forms
# ---------------------------------------------------------------------------
def generate_all_pdfs(result: TaxResult, output_dir: str) -> dict:
    """Generate all tax form PDFs and save to output directory."""
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)
    paths = {}

    # Summary cover page (ReportLab branded)
    buf = generate_summary_page(result)
    p = out / "summary.pdf"
    p.write_bytes(buf.read())
    paths["summary"] = str(p)

    # Form 1040 (official IRS template)
    buf = generate_1040(result)
    p = out / "form_1040.pdf"
    p.write_bytes(buf.read())
    paths["1040"] = str(p)

    # Schedule C (if business income)
    if result.sched_c_businesses:
        buf = generate_schedule_c(result)
        p = out / "schedule_c.pdf"
        p.write_bytes(buf.read())
        paths["schedule_c"] = str(p)

    # Schedule SE (if self-employment tax)
    if result.se_tax > 0:
        buf = generate_schedule_se(result)
        p = out / "schedule_se.pdf"
        p.write_bytes(buf.read())
        paths["schedule_se"] = str(p)

    # Schedule A (if itemizing or for reference)
    buf = generate_schedule_a(result)
    p = out / "schedule_a.pdf"
    p.write_bytes(buf.read())
    paths["schedule_a"] = str(p)

    # Schedule B (if interest/dividends)
    if result.sched_b_interest_total > 0 or result.sched_b_dividends_total > 0:
        buf = generate_schedule_b(result)
        p = out / "schedule_b.pdf"
        p.write_bytes(buf.read())
        paths["schedule_b"] = str(p)

    # Schedule D (if capital transactions)
    if result.capital_transactions:
        buf = generate_schedule_d(result)
        p = out / "schedule_d.pdf"
        p.write_bytes(buf.read())
        paths["schedule_d"] = str(p)

    # Schedule 2 + Form 8959 + Form 8960 (if surtaxes apply)
    if result.niit > 0 or result.additional_medicare_tax > 0 or result.se_tax > 0:
        buf = generate_schedule_2(result)
        p = out / "schedule_2.pdf"
        p.write_bytes(buf.read())
        paths["schedule_2"] = str(p)

    if result.additional_medicare_tax > 0:
        buf = generate_form_8959(result)
        p = out / "form_8959.pdf"
        p.write_bytes(buf.read())
        paths["form_8959"] = str(p)

    if result.niit > 0:
        buf = generate_form_8960(result)
        p = out / "form_8960.pdf"
        p.write_bytes(buf.read())
        paths["form_8960"] = str(p)

    # State returns — dispatch by state
    for sr in getattr(result, 'state_returns', []):
        if sr.state_code == "IL":
            # IL has official fillable template
            buf = generate_il1040(result)
            p = out / "il_1040.pdf"
            p.write_bytes(buf.read())
            paths["il_1040"] = str(p)
        elif sr.total_tax > 0 or sr.withholding > 0:
            # Generic ReportLab summary for other states
            buf = generate_generic_state_summary(sr)
            slug = sr.state_code.lower()
            fname = f"state_{slug}_{sr.return_type}.pdf"
            p = out / fname
            p.write_bytes(buf.read())
            paths[f"state_{slug}_{sr.return_type}"] = str(p)

    # Fallback: if no state_returns but IL fields populated (legacy path)
    if not getattr(result, 'state_returns', []) and result.il_line_11_tax > 0:
        buf = generate_il1040(result)
        p = out / "il_1040.pdf"
        p.write_bytes(buf.read())
        paths["il_1040"] = str(p)

    return paths
