def _generate_cover_page(result: TaxResult, form_list: list[tuple[str, int]]) -> BytesIO:
    """Generate a cover page for the full return PDF package."""
    buf = BytesIO()
    c = canvas.Canvas(buf, pagesize=letter)
    width, height = letter

    # Header
    c.setFont("Helvetica-Bold", 18)
    c.drawCentredString(width / 2, height - 60, "Tax Return Package")
    c.setFont("Helvetica", 10)
    c.drawCentredString(width / 2, height - 80, f"Tax Year {result.tax_year}  |  Generated by TaxLens")

    y = height - 120

    # Filer info
    c.setFont("Helvetica-Bold", 12)
    c.drawString(72, y, "Taxpayer Information")
    y -= 20
    c.setFont("Helvetica", 10)
    filer = result.filer
    if filer:
        c.drawString(90, y, f"Name: {filer.first_name} {filer.last_name}")
        y -= 16
        c.drawString(90, y, f"SSN: {filer.ssn}")
        y -= 16
        if filer.address_street:
            c.drawString(90, y, f"Address: {filer.address_street}")
            y -= 16
            c.drawString(90, y, f"{filer.address_city}, {filer.address_state} {filer.address_zip}")
            y -= 16
    c.drawString(90, y, f"Filing Status: {result.filing_status.replace('_', ' ').title()}")
    y -= 30

    # Key totals
    c.setFont("Helvetica-Bold", 12)
    c.drawString(72, y, "Return Summary")
    y -= 20
    c.setFont("Helvetica", 10)
    totals = [
        ("Total Income", result.line_9_total_income),
        ("Adjusted Gross Income", result.line_11_agi),
        ("Taxable Income", result.line_15_taxable_income),
        ("Total Tax", result.line_24_total_tax),
        ("Total Payments", result.line_33_total_payments),
    ]
    refund_or_owed = result.line_34_overpaid if result.line_34_overpaid > 0 else -result.line_37_owed
    totals.append(("Refund / (Amount Owed)", refund_or_owed))

    for label, val in totals:
        c.drawString(90, y, label)
        sign = "" if val >= 0 else "-"
        c.drawRightString(400, y, f"{sign}${abs(val):,.2f}")
        y -= 16

    y -= 20

    # Table of contents
    c.setFont("Helvetica-Bold", 12)
    c.drawString(72, y, "Included Forms")
    y -= 20
    c.setFont("Helvetica", 10)
    for form_title, page_num in form_list:
        c.drawString(90, y, form_title)
        c.drawRightString(500, y, f"Page {page_num}")
        y -= 14
        if y < 72:
            c.showPage()
            y = height - 50
            c.setFont("Helvetica", 10)

    # DRAFT watermark
    c.saveState()
    c.setFont("Helvetica-Bold", 60)
    c.setFillAlpha(0.1)
    c.translate(300, 400)
    c.rotate(45)
    c.drawCentredString(0, 0, "DRAFT")
    c.restoreState()

    c.save()
    buf.seek(0)
    return buf


def generate_full_return_pdf(result: TaxResult, output_dir: str) -> BytesIO:
    """Generate a single merged PDF containing all tax forms with bookmarks.

    Args:
        result: Computed TaxResult
        output_dir: Directory containing individual form PDFs (from generate_all_pdfs)

    Returns:
        BytesIO with the merged PDF
    """
    out = Path(output_dir)
    merger = pypdf.PdfWriter()

    # Collect available forms in IRS order
    available = []
    for key, title in _FORM_ORDER:
        # Check for numbered variants (1099r_2, etc.)
        if key == "1099r":
            # Check for multiple 1099-R files
            base = out / "1099r.pdf"
            if base.exists():
                available.append((key, title, str(base)))
            for i in range(2, 20):
                variant = out / f"1099r_{i}.pdf"
                if variant.exists():
                    available.append((f"1099r_{i}", f"Form 1099-R ({i})", str(variant)))
            continue

        # Standard file name mapping
        file_names = {
            "summary": "summary.pdf",
            "1040": "form_1040.pdf",
            "1040es": "form_1040es.pdf",
        }
        fname = file_names.get(key, f"{key}.pdf")
        fpath = out / fname
        if fpath.exists():
            available.append((key, title, str(fpath)))

    # Also pick up state returns
    for f in sorted(out.glob("state_*.pdf")):
        state_name = f.stem.replace("state_", "").replace("_", " ").upper()
        available.append((f.stem, f"State: {state_name}", str(f)))
    for f in sorted(out.glob("il_*.pdf")):
        available.append((f.stem, "IL-1040", str(f)))

    # Build page index for cover page
    page_num = 2  # Cover page is page 1
    form_list = []
    for key, title, fpath in available:
        form_list.append((title, page_num))
        reader = pypdf.PdfReader(fpath)
        page_num += len(reader.pages)

    # Generate and prepend cover page
    cover_buf = _generate_cover_page(result, form_list)
    cover_reader = pypdf.PdfReader(cover_buf)
    for page in cover_reader.pages:
        merger.add_page(page)

    # Merge all forms with bookmarks
    for key, title, fpath in available:
        reader = pypdf.PdfReader(fpath)
        bookmark_page = len(merger.pages)
        for page in reader.pages:
            merger.add_page(page)
        merger.add_outline_item(title, bookmark_page)

    buf = BytesIO()
    merger.write(buf)
    buf.seek(0)
    return buf


