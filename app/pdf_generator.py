# Schedule A — Itemized Deductions
# ---------------------------------------------------------------------------
def generate_schedule_a(result: TaxResult) -> BytesIO:
    buf = BytesIO()
    c = canvas.Canvas(buf, pagesize=letter)

    y = HEIGHT - 40
    y = draw_header(c, "Schedule A", "Itemized Deductions", result.tax_year, y)
    y = draw_filer_info(c, result, y)

    y = draw_section(c, "Medical and Dental Expenses", y)
    y = draw_line(c, "1", "Medical and dental expenses", result.sched_a_medical + result.line_11_agi * 0.075 if result.sched_a_medical > 0 else 0, y)
    y = draw_line(c, "3", f"7.5% of AGI ({fmt(result.line_11_agi)} × 7.5%)", result.line_11_agi * 0.075, y)
    y = draw_line(c, "4", "Deductible medical expenses", result.sched_a_medical, y, bold=True)

    y = draw_section(c, "Taxes You Paid", y)
    y = draw_line(c, "5a", "State and local income taxes", 0, y)  # computed in SALT
    y = draw_line(c, "5b", "State and local real estate taxes", 0, y)
    y = draw_line(c, "5d", "Total state/local taxes (SALT — capped at $10,000)", result.sched_a_salt, y, bold=True)

    y = draw_section(c, "Interest You Paid", y)
    y = draw_line(c, "8a", "Home mortgage interest", result.sched_a_mortgage_interest, y, bold=True)

    y = draw_section(c, "Gifts to Charity", y)
    y = draw_line(c, "12", "Gifts by cash or check", result.sched_a_charitable, y, bold=True)

    y -= 10
    y = draw_section(c, "Total Itemized Deductions", y)
    y = draw_line(c, "17", "TOTAL ITEMIZED DEDUCTIONS", result.sched_a_total, y, bold=True)

    c.setFont("Helvetica", 8)
    c.setFillColor(colors.gray)
    y -= 10
    std = result.standard_deduction
    if result.sched_a_total > std:
        c.drawString(58, y, f"Itemized ({fmt(result.sched_a_total)}) exceeds standard deduction ({fmt(std)}) — itemizing saves ${result.sched_a_total - std:,.2f}")
    else:
        c.drawString(58, y, f"Standard deduction ({fmt(std)}) exceeds itemized ({fmt(result.sched_a_total)}) — standard deduction is better")
    c.setFillColor(colors.black)

    c.showPage()
    c.save()
    buf.seek(0)
    return buf


