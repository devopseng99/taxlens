# Schedule D — Capital Gains and Losses
# ---------------------------------------------------------------------------
def generate_schedule_d(result: TaxResult) -> BytesIO:
    buf = BytesIO()
    c = canvas.Canvas(buf, pagesize=letter)

    y = HEIGHT - 40
    y = draw_header(c, "Schedule D", "Capital Gains and Losses", result.tax_year, y)
    y = draw_filer_info(c, result, y)

    # Part I — Short-term
    y = draw_section(c, "Part I — Short-Term Capital Gains and Losses (held one year or less)", y)

    short_txns = [t for t in result.capital_transactions if not t.is_long_term]
    for t in short_txns:
        c.setFont("Helvetica", 7)
        c.drawString(65, y, f"{t.description}")
        c.drawString(220, y, f"Acquired: {t.date_acquired}")
        c.drawString(320, y, f"Sold: {t.date_sold}")
        c.drawString(410, y, f"Proceeds: ${t.proceeds:,.2f}")
        c.drawString(500, y, f"Gain/Loss: ${t.gain_loss:,.2f}")
        y -= 12

    y = draw_line(c, "7", "Net short-term capital gain or (loss)", result.sched_d_short_term_gain, y, bold=True)

    # Part II — Long-term
    y = draw_section(c, "Part II — Long-Term Capital Gains and Losses (held more than one year)", y)

    long_txns = [t for t in result.capital_transactions if t.is_long_term]
    for t in long_txns:
        c.setFont("Helvetica", 7)
        c.drawString(65, y, f"{t.description}")
        c.drawString(220, y, f"Acquired: {t.date_acquired}")
        c.drawString(320, y, f"Sold: {t.date_sold}")
        c.drawString(410, y, f"Proceeds: ${t.proceeds:,.2f}")
        c.drawString(500, y, f"Gain/Loss: ${t.gain_loss:,.2f}")
        y -= 12

    y = draw_line(c, "15", "Net long-term capital gain or (loss)", result.sched_d_long_term_gain, y, bold=True)

    # Part III — Summary
    y = draw_section(c, "Part III — Summary", y)
    y = draw_line(c, "16", "Net capital gain or (loss) — to Form 1040, Line 7", result.sched_d_net_gain, y, bold=True)

    c.showPage()
    c.save()
    buf.seek(0)
    return buf


