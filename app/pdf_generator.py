from tax_engine import TaxResult

WIDTH, HEIGHT = letter
TEMPLATE_DIR = Path(__file__).parent / "templates"


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------
def _money(val: float) -> str:
    """Format a dollar value for IRS forms (no $ sign, round to whole)."""
    if val == 0:
        return ""
    return str(round(val))


def _money2(val: float) -> str:
    """Format with cents for specific fields."""
    if val == 0:
        return ""
    return f"{val:.2f}"


def _fill_pdf(template_path: str, field_map: dict) -> BytesIO:
    """Fill a PDF template and return as BytesIO.

    field_map: {field_name: value} — uses full dotted field names from pypdf.
    """
    reader = pypdf.PdfReader(template_path)
    writer = pypdf.PdfWriter(clone_from=reader)

    # Update form fields on each page
    for page_num in range(len(writer.pages)):
        writer.update_page_form_field_values(
            writer.pages[page_num], field_map, auto_regenerate=False
        )

    buf = BytesIO()
    writer.write(buf)
    buf.seek(0)
    return buf


def _set_checkbox(field_map: dict, field_name: str, checked: bool = True):
    """Set a checkbox field. IRS PDFs use /1 for checked, /Off for unchecked."""
    if checked:
        field_map[field_name] = "/1"


# ---------------------------------------------------------------------------
# ReportLab helpers (for summary page only)
# ---------------------------------------------------------------------------
def fmt(amount: float) -> str:
    if amount == 0:
        return "—"
    if amount < 0:
        return f"({abs(amount):,.2f})"
    return f"{amount:,.2f}"


def draw_header(c: canvas.Canvas, form_name: str, form_title: str, tax_year: int, y: float) -> float:
    c.setStrokeColor(colors.HexColor("#1a237e"))
    c.setLineWidth(2)
    c.line(50, y, WIDTH - 50, y)
    y -= 5
    c.setFont("Helvetica-Bold", 18)
    c.setFillColor(colors.HexColor("#1a237e"))
    c.drawString(55, y - 20, form_name)
    c.setFont("Helvetica-Bold", 11)
    c.drawCentredString(WIDTH / 2, y - 18, form_title)
    c.setFont("Helvetica-Bold", 14)
    c.drawRightString(WIDTH - 55, y - 20, str(tax_year))
    c.setFont("Helvetica", 7)
    c.setFillColor(colors.gray)
    c.drawCentredString(WIDTH / 2, y - 30, "Generated by TaxLens — DRAFT for review purposes only")
    y -= 45
    c.setStrokeColor(colors.HexColor("#1a237e"))
    c.setLineWidth(1)
    c.line(50, y, WIDTH - 50, y)
    c.setFillColor(colors.black)
    return y - 10


def draw_section(c: canvas.Canvas, title: str, y: float) -> float:
    c.setFont("Helvetica-Bold", 9)
    c.setFillColor(colors.HexColor("#1a237e"))
    c.drawString(55, y, title)
    c.setLineWidth(0.5)
    c.setStrokeColor(colors.HexColor("#bbdefb"))
    c.line(55, y - 3, WIDTH - 55, y - 3)
    c.setFillColor(colors.black)
    return y - 16


def draw_line(c: canvas.Canvas, line_num: str, description: str, amount: float, y: float,
              bold: bool = False, indent: int = 0) -> float:
    font = "Helvetica-Bold" if bold else "Helvetica"
    c.setFont(font, 8)
    x_num = 58 + indent
    x_desc = 80 + indent
    x_amt = WIDTH - 60
    if line_num:
        c.setFillColor(colors.HexColor("#1a237e"))
        c.drawString(x_num, y, line_num)
    c.setFillColor(colors.black)
    c.drawString(x_desc, y, description)
    if bold:
        c.setStrokeColor(colors.HexColor("#1a237e"))
        c.setFillColor(colors.HexColor("#e8eaf6"))
        c.rect(x_amt - 80, y - 3, 130, 13, fill=1, stroke=1)
        c.setFillColor(colors.HexColor("#1a237e"))
    else:
        c.setStrokeColor(colors.HexColor("#e0e0e0"))
        c.rect(x_amt - 80, y - 3, 130, 13, fill=0, stroke=1)
        c.setFillColor(colors.black)
    c.setFont(font, 8)
    c.drawRightString(x_amt + 45, y, fmt(amount))
    c.setFillColor(colors.black)
    return y - 16


def draw_info_row(c: canvas.Canvas, label: str, value: str, y: float, x: float = 58) -> float:
    c.setFont("Helvetica", 7)
    c.setFillColor(colors.gray)
    c.drawString(x, y, label)
    c.setFillColor(colors.black)
    c.setFont("Helvetica", 9)
    c.drawString(x + 80, y, value)
    return y - 14


