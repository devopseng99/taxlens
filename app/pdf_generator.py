# Schedule D — Capital Gains and Losses
# ---------------------------------------------------------------------------
def generate_schedule_d(result: TaxResult) -> BytesIO:
    template = str(TEMPLATE_DIR / "f1040sd.pdf")
    f = {}
    p1 = "topmostSubform[0].Page1[0]."
    filer = result.filer

    f[p1 + "f1_1[0]"] = f"{filer.first_name} {filer.last_name}"
    f[p1 + "f1_2[0]"] = filer.ssn

    # Part I — Short-term (rows 1a-3, then line 7)
    short_txns = [t for t in result.capital_transactions if not t.is_long_term]
    for i, t in enumerate(short_txns[:3]):
        row_base = f"Table_PartI[0].Row{['1a','1b','2'][i]}[0]."
        idx_start = 3 + i * 4
        f[p1 + row_base + f"f1_{idx_start}[0]"] = t.description
        f[p1 + row_base + f"f1_{idx_start+1}[0]"] = t.date_acquired
        f[p1 + row_base + f"f1_{idx_start+2}[0]"] = t.date_sold
        f[p1 + row_base + f"f1_{idx_start+3}[0]"] = _money(t.proceeds)
        # Cost basis and gain in adjacent columns
        f[p1 + row_base + f"f1_{idx_start+4}[0]"] = _money(t.cost_basis)
        f[p1 + row_base + f"f1_{idx_start+6}[0]"] = _money(t.gain_loss)
    f[p1 + "f1_19[0]"] = _money(result.sched_d_short_term_gain)     # Line 7

    # Part II — Long-term (rows 8a-10, then line 15)
    long_txns = [t for t in result.capital_transactions if t.is_long_term]
    for i, t in enumerate(long_txns[:3]):
        row_base = f"Table_PartII[0].Row{['8a','8b','9'][i]}[0]."
        idx_start = 23 + i * 4
        f[p1 + row_base + f"f1_{idx_start}[0]"] = t.description
        f[p1 + row_base + f"f1_{idx_start+1}[0]"] = t.date_acquired
        f[p1 + row_base + f"f1_{idx_start+2}[0]"] = t.date_sold
        f[p1 + row_base + f"f1_{idx_start+3}[0]"] = _money(t.proceeds)
        f[p1 + row_base + f"f1_{idx_start+4}[0]"] = _money(t.cost_basis)
        f[p1 + row_base + f"f1_{idx_start+6}[0]"] = _money(t.gain_loss)
    f[p1 + "f1_39[0]"] = _money(result.sched_d_long_term_gain)      # Line 15

    # Part III
    f[p1 + "f1_43[0]"] = _money(result.sched_d_net_gain)            # Line 16

    return _fill_pdf(template, f)


