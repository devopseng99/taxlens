"""PDF tax form generator — fills official IRS/IL fillable PDF templates.

Uses pypdf to populate actual government form fields with computed tax data.
Templates: IRS Form 1040, Schedules A/B/C/D/SE, IL-1040.
Keeps ReportLab summary page as a TaxLens-branded cover sheet.
"""

import copy
